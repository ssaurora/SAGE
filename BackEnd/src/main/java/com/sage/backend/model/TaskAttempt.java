package com.sage.backend.model;

import java.time.OffsetDateTime;

public class TaskAttempt {
    private Long id;
    private String taskId;
    private Integer attemptNo;
    private String trigger;
    private String jobId;
    private String statusSnapshotJson;
    private OffsetDateTime createdAt;
    private OffsetDateTime finishedAt;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTaskId() {
        return taskId;
    }

    public void setTaskId(String taskId) {
        this.taskId = taskId;
    }

    public Integer getAttemptNo() {
        return attemptNo;
    }

    public void setAttemptNo(Integer attemptNo) {
        this.attemptNo = attemptNo;
    }

    public String getTrigger() {
        return trigger;
    }

    public void setTrigger(String trigger) {
        this.trigger = trigger;
    }

    public String getJobId() {
        return jobId;
    }

    public void setJobId(String jobId) {
        this.jobId = jobId;
    }

    public String getStatusSnapshotJson() {
        return statusSnapshotJson;
    }

    public void setStatusSnapshotJson(String statusSnapshotJson) {
        this.statusSnapshotJson = statusSnapshotJson;
    }

    public OffsetDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(OffsetDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public OffsetDateTime getFinishedAt() {
        return finishedAt;
    }

    public void setFinishedAt(OffsetDateTime finishedAt) {
        this.finishedAt = finishedAt;
    }
}

